def match_factor_terms(query: str) -> list[str]:
    text = _normalize_text(query)
    matched: list[str] = []
    for term, factor_names in FACTOR_TERM_MAP.items():
        if _normalize_text(term) in text:
            matched.extend(factor_names)
    return list(dict.fromkeys(matched))


def match_category_terms(query: str) -> list[str]:
    text = _normalize_text(query)
    matched: list[str] = []
    for term, category in CATEGORY_TERM_MAP.items():
        if _normalize_text(term) in text:
            matched.append(category)
    return list(dict.fromkeys(matched))


def extract_target_horizon(query: str) -> int | None:
    text = _normalize_text(query)
    for term, horizon in sorted(TARGET_HORIZON_PATTERNS.items(), key=lambda item: len(item[0]), reverse=True):
        if _normalize_text(term) in text:
            return horizon
    return None


def find_ambiguous_terms(query: str) -> list[str]:
    text = _normalize_text(query)
    matched: list[str] = []
    for term, candidates in AMBIGUOUS_TERM_MAP.items():
        if _normalize_text(term) in text:
            matched.extend(candidates)
    return list(dict.fromkeys(matched))


def get_expression_kb() -> list[dict]:
    return [_entry_to_dict(entry) for entry in EXPRESSION_KB]


def get_expression_by_phrase(phrase: str) -> Optional[dict]:
    query = phrase.strip().lower()
    for entry in EXPRESSION_KB:
        candidates = [entry.phrase, entry.canonical_name] + entry.synonyms + entry.example_queries
        if any(query == str(item).lower() for item in candidates):
            return _entry_to_dict(entry)
    return None


def search_expressions(query: str, limit: int = 10) -> list[dict]:
    query_lower = _normalize_text(query)
    if not query_lower:
        return []

    results = []
    seen = set()
    for entry in EXPRESSION_KB:
        if _expression_matches_query(entry, query_lower):
            if entry.id in seen:
                continue
            seen.add(entry.id)
            results.append(_entry_to_dict(entry))
    return sorted(
        results,
        key=lambda item: (-_expression_score(item, query_lower), -len(item.get("phrase", ""))),
    )[:limit]


def classify_expression(query: str) -> dict:
    matches = search_expressions(query, limit=8)
    if not matches:
        return {
            "query": query,
            "matched": [],
            "best_match": None,
            "route_intent": ["manual_review"],
            "route_to": ["manual_review"],
            "leakage_warning": False,
            "matched_layers": [],
        }

    best = _pick_best_match(query, matches)
    route_intent = best.get("route_intent", [])
    route_to = best.get("route_to", [])
    leakage_warning = any(item.get("leakage_risk") for item in matches)
    return {
        "query": query,
        "matched": matches,
        "best_match": best,
        "route_intent": route_intent,
        "route_to": route_to,
        "leakage_warning": leakage_warning,
        "matched_layers": sorted({item.get("layer", "") for item in matches if item.get("layer")}),
    }


def _pick_best_match(query: str, matches: list[dict]) -> dict:
    query_lower = _normalize_text(query)
    exact = [item for item in matches if _normalize_text(item.get("phrase", "")) in query_lower or _normalize_text(item.get("canonical_name", "")) in query_lower]
    if exact:
        return sorted(exact, key=lambda item: len(item.get("phrase", "")), reverse=True)[0]
    target_priority = {
        "target_expression": 0,
        "condition_expression": 1,
        "factor_expression": 2,
        "metric_expression": 3,
        "sort_expression": 4,
        "intent_expression": 5,
    }
    return sorted(
        matches,
        key=lambda item: (
            target_priority.get(item.get("expression_type", ""), 99),
            -_expression_score(item, query_lower),
            len(item.get("phrase", "")),
        ),
    )[0]


def _normalize_text(text: str) -> str:
    text = (text or "").strip().lower()
    for ch in ["，", ",", "。", "；", ";", " ", "？", "?", "！", "!", "：", ":", "（", "）", "(", ")"]:
        text = text.replace(ch, "")
    return text


def _expression_matches_query(entry: ExpressionEntry, query_lower: str) -> bool:
    candidates = [
        entry.phrase,
        entry.canonical_name,
        entry.meaning,
        entry.formula,
    ] + entry.synonyms + entry.example_queries
    normalized_candidates = [_normalize_text(str(item)) for item in candidates]
    return any(
        candidate and (candidate in query_lower or query_lower in candidate)
        for candidate in normalized_candidates
    )


def _expression_score(entry: dict, query_lower: str) -> int:
    candidates = [
        entry.get("phrase", ""),
        entry.get("canonical_name", ""),
        entry.get("meaning", ""),
        entry.get("formula", ""),
    ] + entry.get("synonyms", []) + entry.get("example_queries", [])
    query_tokens = [token for token in _tokenize(query_lower) if token]
    best = 0
    for item in candidates:
        candidate = _normalize_text(str(item))
        if not candidate:
            continue
        score = 0
        if candidate in query_lower:
            score += len(candidate)
        if query_lower in candidate:
            score += max(1, len(query_lower))
        score += sum(1 for token in query_tokens if token and token in candidate)
        best = max(best, score)
    return best


def _tokenize(text: str) -> list[str]:
    tokens = []
    for sep in ["且", "和", "或", "与", "以及", "、"]:
        text = text.replace(sep, "|")
    for chunk in text.split("|"):
        chunk = chunk.strip()
        if chunk:
            tokens.append(chunk)
    return tokens


def _entry_to_dict(entry: ExpressionEntry) -> dict:
    result = entry.__dict__.copy()
    if not result.get("layer"):
        result["layer"] = _infer_layer(entry)
    return result


def _infer_layer(entry: ExpressionEntry) -> str:
    if entry.expression_type == "target_expression":
        return "target"
    if entry.expression_type == "metric_expression":
        return "composite_score"
    if entry.expression_type in {"condition_expression", "factor_expression"}:
        return "processed_factor"
    return ""


def list_expression_layers() -> dict[str, int]:
    counts: dict[str, int] = {}
    for entry in EXPRESSION_KB:
        layer = _entry_to_dict(entry).get("layer", "") or "unknown"
        counts[layer] = counts.get(layer, 0) + 1
    return dict(sorted(counts.items(), key=lambda item: item[0]))
