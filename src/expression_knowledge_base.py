"""Structured expression knowledge base for AutoETF agent routing.

This module stores common user phrases, their canonical meaning, calculation
rules, and routing hints so the agent can map natural language to executable
research logic more precisely.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Optional


@dataclass(frozen=True)
class ExpressionEntry:
    id: str
    phrase: str
    canonical_name: str
    expression_type: str
    category: str
    meaning: str
    layer: str = ""
    synonyms: list[str] = field(default_factory=list)
    required_columns: list[str] = field(default_factory=list)
    derived_columns: list[str] = field(default_factory=list)
    formula: str = ""
    calculation_hint: str = ""
    default_window: Optional[int] = None
    optional_windows: list[int] = field(default_factory=list)
    operator: str = ""
    threshold: Any = None
    threshold_type: str = ""
    data_dependency: str = "ohlcv"
    time_direction: str = "current"
    can_be_condition: bool = False
    can_be_factor: bool = False
    can_be_target: bool = False
    leakage_risk: bool = False
    route_to: list[str] = field(default_factory=list)
    ambiguity_level: str = "low"
    fallback_policy: str = "manual_review"
    example_queries: list[str] = field(default_factory=list)
    related_expressions: list[str] = field(default_factory=list)
    correlated_with: list[str] = field(default_factory=list)
    negative_cases: list[str] = field(default_factory=list)
    route_intent: list[str] = field(default_factory=list)
    interpretation: dict[str, str] = field(default_factory=dict)
    knowledge_status: str = "known"
    implementation_status: str = "implemented"
    implementation_key: str = ""
    selection_group: str = ""
    selection_priority: int = 50
    required_context: list[str] = field(default_factory=list)


